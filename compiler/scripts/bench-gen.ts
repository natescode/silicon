/**
 * Bench fixture generator.  Produces medium.si (~500 LOC) and large.si
 * (~5000 LOC) by repeating a parametric pattern.  Deterministic — same
 * seed produces the same file.  Run via `bun run scripts/bench-gen.ts`.
 *
 * Why generate rather than commit large fixtures: the bench shape
 * (many @fn definitions, varied call sites) is what we want to measure
 * the compiler under, and committing a 5000-LOC file would bloat the
 * repo for no benefit.
 */

import { writeFileSync, mkdirSync } from 'fs'
import { join, dirname } from 'path'

const HEADER = `# SPDX-License-Identifier: MIT
# Generated by scripts/bench-gen.ts.  Do not edit by hand — re-run
# the generator instead.

`

function emit(funcCount: number): string {
    const out: string[] = [HEADER]
    out.push('@struct Pair lo:Int, hi:Int;\n')
    out.push('@type Maybe[T] := $Just v:T | $Nothing;\n\n')

    for (let i = 0; i < funcCount; i++) {
        const a = (i * 17) % 257
        const b = (i * 31 + 5) % 257
        out.push(`@fn f${i} x:Int := {`)
        out.push(`    @let p:Pair := &Pair x, ${a};`)
        out.push(`    @let r:Int := p.lo + p.hi * ${b};`)
        if (i % 5 === 0) {
            out.push(`    &@if r < 0, { &@return 0 };`)
        }
        if (i % 7 === 0) {
            out.push(`    @var acc:Int := 0;`)
            out.push(`    @var k:Int := 1;`)
            out.push(`    &@loop k <= 10, { acc = acc + k * ${a}; k = k + 1; };`)
            out.push(`    r + acc`)
        } else {
            out.push(`    r`)
        }
        out.push(`};\n`)
    }

    out.push('@fn bench_main := {')
    out.push('    @var total:Int := 0;')
    for (let i = 0; i < Math.min(funcCount, 50); i++) {
        out.push(`    total = total + (&f${i} ${i});`)
    }
    out.push('    total')
    out.push('};')
    out.push('')
    out.push('@export bench_main;')

    return out.join('\n')
}

function lineCount(s: string): number {
    return s.split('\n').length
}

function writeFixture(filename: string, funcCount: number): void {
    const src = emit(funcCount)
    const lines = lineCount(src)
    const path = join(import.meta.dirname, '..', 'tests', 'bench', 'fixtures', filename)
    mkdirSync(dirname(path), { recursive: true })
    writeFileSync(path, src, 'utf-8')
    console.log(`wrote ${path} — ${funcCount} fns, ${lines} LOC`)
}

writeFixture('medium.si', 50)  // ~500 LOC
writeFixture('large.si', 500)  // ~5000 LOC
